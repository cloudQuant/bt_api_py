def split_ctp_wrapper(ctp_py_path=None, output_dir=None, dry_run=False):
    """执行拆分: 解析 ctp.py → 提取代码 → 生成子模块 → 替换 ctp.py。

    Args:
        ctp_py_path: ctp.py 的路径，默认为 bt_api_py/ctp/ctp.py
        output_dir: 输出目录，默认与 ctp.py 同目录
        dry_run: 如果 True，只打印不写文件

    Returns:
        bool: 是否成功
    """
    if ctp_py_path is None:
        ctp_py_path = _CTP_PY_PATH
    if output_dir is None:
        output_dir = os.path.dirname(ctp_py_path)

    if not os.path.exists(ctp_py_path):
        print(f"WARNING: {ctp_py_path} not found, skipping CTP wrapper split")
        return False

    # 如果 ctp.py 已经是兼容垫片，则跳过
    with open(ctp_py_path, "r", encoding="utf-8") as f:
        head = f.readline()
    if _AUTO_GEN_HEADER.strip() in head:
        print(f"CTP wrapper already split (ctp.py is compat shim), skipping")
        return True

    print(f"Splitting CTP wrapper: {ctp_py_path}")

    # 解析
    preamble_code, constants_code, class_blocks, constant_names, class_names = parse_ctp_py(
        ctp_py_path
    )
    print(f"  Parsed: {len(constant_names)} constants, {len(class_names)} classes")

    # 分类
    module_class_map = OrderedDict()  # module_name -> [class_names]
    for mod_name in _MODULE_DESCRIPTIONS:
        if mod_name != "ctp_constants":
            module_class_map[mod_name] = []

    for cls_name in class_names:
        mod = classify_class(cls_name)
        module_class_map[mod].append(cls_name)

    # 验证
    ok = validate(constant_names, class_names, module_class_map)
    if not ok:
        return False

    # 清理旧文件
    if not dry_run:
        cleanup_old_files(output_dir)

    # 生成 _ctp_base.py
    generate_base_module(preamble_code, output_dir, dry_run)

    # 生成 ctp_constants.py
    generate_constants_module(constants_code, constant_names, output_dir, dry_run)

    # 生成各类子模块
    for mod_name, cls_list in module_class_map.items():
        if cls_list:
            generate_class_module(mod_name, cls_list, class_blocks, output_dir, dry_run)

    # 替换 ctp.py 为兼容垫片
    active_modules = ["ctp_constants"] + [m for m, c in module_class_map.items() if c]
    generate_compat_ctp_py(active_modules, output_dir, dry_run)

    # 汇总
    total_classes = sum(len(c) for c in module_class_map.values())
    total_modules = sum(1 for c in module_class_map.values() if c) + 1  # +1 for constants
    print(
        f"  Total: {total_classes} classes + {len(constant_names)} constants "
        f"in {total_modules} modules + _ctp_base + ctp.py shim"
    )

    return ok


def main():
    import argparse

    parser = argparse.ArgumentParser(
        description="Split SWIG-generated ctp.py into sub-modules (v2: real code extraction)"
    )
    parser.add_argument("--ctp-py", default=None, help="Path to ctp.py")
    parser.add_argument("--output-dir", default=None, help="Output directory")
    parser.add_argument("--dry-run", action="store_true", help="Print plan without writing files")
    args = parser.parse_args()

    ok = split_ctp_wrapper(
        ctp_py_path=args.ctp_py,
        output_dir=args.output_dir,
        dry_run=args.dry_run,
    )
    sys.exit(0 if ok else 1)


if __name__ == "__main__":
    main()
